// ----Recursive------Fibobacci-----------

int main()
{
    int ans;
    ans = fact(4);
    printf("%d", ans);
    return 0;
}

int fact(int n){
	int ans;
	if(n == 1)
	  ans = 1;
    else
	  ans = n*fact(n-1);
 
	return ans;
}
